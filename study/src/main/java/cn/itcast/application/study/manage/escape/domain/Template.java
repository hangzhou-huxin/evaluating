package cn.itcast.application.study.manage.escape.domain;

public class Template {
	
	private Integer id ;
	
	
	private String	content ;
	
	
	private String	memo ;
	
	
	private Integer	scoreSectionId ;


	public Integer getId() {
		return id;
	}


	public void setId(Integer id) {
		this.id = id;
	}


	public String getContent() {
		return content;
	}


	public void setContent(String content) {
		this.content = content;
	}


	public String getMemo() {
		return memo;
	}


	public void setMemo(String memo) {
		this.memo = memo;
	}


	public Integer getScoreSectionId() {
		return scoreSectionId;
	}


	public void setScoreSectionId(Integer scoreSectionId) {
		this.scoreSectionId = scoreSectionId;
	}
	
	
	

}
